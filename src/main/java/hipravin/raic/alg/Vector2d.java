package hipravin.raic.alg;

public class Vector2d {
    private final double dx;
    private final double dy;

    public Vector2d(double dx, double dy) {
        this.dx = dx;
        this.dy = dy;
    }

    public double getDx() {
        return dx;
    }

    public double getDy() {
        return dy;
    }
}
